package application;

public class Student {
    String name, email;

    public Student(String name, String email) {
        this.name = name;
        this.email = email;
    }





    //getter n setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
